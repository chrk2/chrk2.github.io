// Обновить счетчик корзины
function updateCartCounter() {
    const badges = document.querySelectorAll('.cart-counter-badge');
    const totalCount = cart.reduce((sum, item) => sum + item.quantity, 0);
    
    // Обновляем новые бейджи
    badges.forEach(badge => {
        if (totalCount > 0) {
            badge.textContent = totalCount;
            badge.style.display = 'flex';
        } else {
            badge.style.display = 'none';
        }
    });
    
    // Старые счетчики (если есть) - можно оставить для совместимости
    const oldCounters = document.querySelectorAll('.cart-counter');
    oldCounters.forEach(counter => {
        if (totalCount > 0) {
            counter.textContent = totalCount;
            counter.style.display = 'inline';
        } else {
            counter.style.display = 'none';
        }
    });
    
    localStorage.setItem('cart', JSON.stringify(cart));
}

// Добавить в корзину (для каталога)
function addToCart(productId, quantity = 1) {
    const product = products.find(p => p.id === productId);
    if (!product) return;
    
    const existingItem = cart.find(item => item.id === productId);
    
    if (existingItem) {
        existingItem.quantity += quantity;
    } else {
        cart.push({
            id: product.id,
            name: product.name,
            price: product.price,
            priceFormatted: product.priceFormatted,
            image: product.image,
            quantity: quantity
        });
    }
    
    updateCartCounter();
    
    const btn = event?.target;
    if (btn) {
        btn.classList.add('added-to-cart');
        setTimeout(() => btn.classList.remove('added-to-cart'), 500);
    }
    
    showNotification(` ${product.name} добавлен в корзину`);
}

// Удалить из корзины
function removeFromCart(productId) {
    cart = cart.filter(item => item.id !== productId);
    updateCartCounter();
    if (window.location.pathname.includes('cart.html')) {
        displayCart();
    }
}

// Изменить количество
function updateQuantity(productId, newQuantity) {
    const item = cart.find(item => item.id === productId);
    if (item) {
        if (newQuantity <= 0) {
            removeFromCart(productId);
        } else {
            item.quantity = newQuantity;
            updateCartCounter();
            if (window.location.pathname.includes('cart.html')) {
                displayCart();
            }
        }
    }
}

// Очистить корзину
function clearCart() {
    cart = [];
    updateCartCounter();
    if (window.location.pathname.includes('cart.html')) {
        displayCart();
    }
}

// Подсчет итоговой суммы
function getCartTotal() {
    return cart.reduce((sum, item) => sum + (item.price * item.quantity), 0);
}

// Форматирование цены
function formatPrice(price) {
    return price.toString().replace(/\B(?=(\d{3})+(?!\d))/g, " ") + " 〒";
}

// Показать уведомление
