function displayProducts(productsToShow, page = 1) {
    const container = document.getElementById('products-container');
    if (!container) return;
    
    const start = (page - 1) * itemsPerPage;
    const end = start + itemsPerPage;
    const paginatedProducts = productsToShow.slice(start, end);
    
    container.innerHTML = '';
    
    paginatedProducts.forEach(product => {
        const col = document.createElement('div');
        col.className = 'col-lg-3 col-md-4 col-sm-6';
        
        const saleBadge = product.sale ? '<div class="product-badge">Хит продаж!</div>' : '';
        
        col.innerHTML = `
            <a href="product.html?id=${product.id}" class="product-item-link">
                <div class="product-item">
                    <div class="product-image-wrapper">
                        ${saleBadge}
                        <img src="${product.image}" alt="${product.name}">
                    </div>
                    <div class="down-content">
                        <h4>${product.name}</h4>
                        <p>${product.description}</p>
                        <h6>${product.priceFormatted}</h6>
                        <span class="filled-button">Подробнее</span>
                    </div>
                </div>
            </a>
        `;
        container.appendChild(col);
    });
    
    updatePagination(productsToShow.length, page);
}

function updatePagination(totalItems, currentPage) {
    const paginationContainer = document.querySelector('.pagination ul');
    if (!paginationContainer) return;
    
    const totalPages = Math.ceil(totalItems / itemsPerPage);
    if (totalPages <= 1) {
        paginationContainer.innerHTML = '';
        return;
    }
    
    let html = '';
    
    html += `<li class="page-item ${currentPage === 1 ? 'disabled' : ''}">
        <a class="page-link" href="#" onclick="changePage(${currentPage - 1}); return false;">←</a>
    </li>`;
    
    for (let i = 1; i <= totalPages; i++) {
        if (i === 1 || i === totalPages || (i >= currentPage - 2 && i <= currentPage + 2)) {
            html += `<li class="page-item ${i === currentPage ? 'active' : ''}">
                <a class="page-link" href="#" onclick="changePage(${i}); return false;">${i}</a>
            </li>`;
        } else if (i === currentPage - 3 || i === currentPage + 3) {
            html += `<li class="page-item disabled"><span class="page-link">...</span></li>`;
        }
    }
    
    html += `<li class="page-item ${currentPage === totalPages ? 'disabled' : ''}">
        <a class="page-link" href="#" onclick="changePage(${currentPage + 1}); return false;">→</a>
    </li>`;
    
    paginationContainer.innerHTML = html;
}

function changePage(newPage) {
    const filterType = document.querySelector('.category-btn.active')?.getAttribute('onclick')?.match(/'([^']+)'/)?.[1] || 'all';
    currentPage = newPage;
    
    let filteredProducts = getFilteredProducts(filterType);
    displayProducts(filteredProducts, newPage);
    window.scrollTo({ top: 0, behavior: 'smooth' });
}

function getFilteredProducts(filterType) {
    if (filterType === 'all') return products;
    if (filterType === 'sale') return products.filter(p => p.sale === true);
    if (filterType === 'top') return products.filter(p => p.top === true);
    return products.filter(p => p.category === filterType);
}

function filterCategory(type) {
    const buttons = document.querySelectorAll('.category-btn');
    if (!buttons.length) return;
    
    buttons.forEach(btn => btn.classList.remove('active'));
    event.target.classList.add('active');
    
    currentPage = 1;
    const filteredProducts = getFilteredProducts(type);
    displayProducts(filteredProducts, 1);
}

