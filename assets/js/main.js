// ========== ОФОРМЛЕНИЕ ЗАКАЗА С ПРОВЕРКОЙ СОГЛАСИЙ ==========
function checkout() {
    if (cart.length === 0) {
        showNotification('Корзина пуста!');
        return;
    }
    
    // Проверяем согласия
    const agreeOferta = document.getElementById('agreeOferta');
    const agreePrivacy = document.getElementById('agreePrivacy');
    
    if (agreeOferta && !agreeOferta.checked) {
        showNotification('Необходимо принять условия публичной оферты');
        agreeOferta.focus();
        return;
    }
    
    if (agreePrivacy && !agreePrivacy.checked) {
        showNotification('Необходимо дать согласие на обработку персональных данных');
        agreePrivacy.focus();
        return;
    }

    const phoneNumber = '77771234567'; // <- ВСТАВЬ СВОЙ НОМЕР
    
    let message = ' НОВЫЙ ЗАКАЗ С САЙТА Vizit\n\n';
    message += 'Товары:\n';
    
    cart.forEach((item, index) => {
        message += `${index + 1}. ${item.name} — ${item.quantity} шт — ${formatPrice(item.price * item.quantity)}\n`;
    });
    
    const total = getCartTotal();
    message += `\nИТОГО: ${formatPrice(total)}`;
    
    const encodedMessage = encodeURIComponent(message);
    const whatsappUrl = `https://wa.me/${phoneNumber}?text=${encodedMessage}`;
    
    window.open(whatsappUrl, '_blank');
    showNotification('✅ Заказ передан в WhatsApp. Ожидайте подтверждения!');
}

